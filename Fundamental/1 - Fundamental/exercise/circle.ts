//circle
const circleRadius: number = 5
const phi: number = 3.14159

//circle diameter
const circleDiameter:number = circleRadius * 2

//circle circumference
const circleCircumference: number = phi * circleDiameter

//circle area
const circleArea:number = phi * circleRadius* circleRadius


console.log(circleCircumference);
console.log(circleDiameter);
console.log(circleArea);
